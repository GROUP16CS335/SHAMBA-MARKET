     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        if($user->role_as == 'vendor'){
            $user->name = $request->input('name');
            $user->email = $request->input('email');
            $user->save();
        }

        return redirect('/vendor-profile/'.\Auth::id())->with('status', 'Updated');
    }

